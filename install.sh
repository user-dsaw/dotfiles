# Ensures paru is available
ensure_paru() {
    if command -v paru &>/dev/null; then
        log_ok "paru found"
        return
    fi
    log_info "paru not found вҖ” installing..."
    sudo pacman -S --needed --noconfirm git base-devel
    local tmp
    tmp="$(mktemp -d)"
    git clone https://aur.archlinux.org/paru.git "$tmp/paru"
    (cd "$tmp/paru" && makepkg -si --noconfirm)
    rm -rf "$tmp"
    log_ok "paru installed"
}

install_packages() {
    echo -e "${GOLD}в”Җв”Җ Packages в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ${NC}"
    ensure_paru

    # Core Hyprland environment
    local packages=(
        hyprland
        waybar
        ghostty
        kitty
        rofi
        swaync
        hyprlock
        wlogout
        swww
        zsh
        stow
        git
        lazygit
        zsh-syntax-highlighting
        zsh-autosuggestions
        fzf
        yazi
        btop
        cava
        fastfetch
        chafa
        pokemon-colorscripts-git
        imagemagick
        bluetuith
        bluez
        bluez-utils
        ttf-anonymouspro-nerd
        playerctl
        grimblast-git
        nwg-look
    )

    local failed=()
    for pkg in "${packages[@]}"; do
        if paru -S --needed --noconfirm "$pkg" &>/dev/null; then
            log_ok "$pkg"
        else
            log_fail "$pkg"
            failed+=("$pkg")
        fi
    done

    if [[ ${#failed[@]} -gt 0 ]]; then
        echo ""
        log_warn "the following packages failed to install:"
        for pkg in "${failed[@]}"; do
            echo "        - $pkg"
        done
        log_warn "you can install them manually with: paru -S ${failed[*]}"
    fi

    echo ""
}

# в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
#  PHASE 3 вҖ” SHELL SETUP (oh-my-zsh + p10k)
# в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
install_shell() {
    echo -e "${GOLD}в”Җв”Җ Shell setup в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ${NC}"

    # Oh My Zsh
    if [[ -d "$TARGET_HOME/.oh-my-zsh" ]]; then
        log_ok "oh-my-zsh already installed"
    else
        log_info "installing oh-my-zsh..."
        RUNZSH=no CHSH=no \
            sh -c "$(curl -fsSL https://raw.githubusercontent.com/ohmyzsh/ohmyzsh/master/tools/install.sh)"
        log_ok "oh-my-zsh installed"
    fi

    # Powerlevel10k theme
    local p10k_dir="${ZSH_CUSTOM:-$TARGET_HOME/.oh-my-zsh/custom}/themes/powerlevel10k"
    if [[ -d "$p10k_dir" ]]; then
        log_ok "powerlevel10k already installed"
    else
        log_info "installing powerlevel10k..."
        git clone --depth=1 https://github.com/romkatv/powerlevel10k.git "$p10k_dir"
        log_ok "powerlevel10k installed"
    fi

    # Set zsh as default shell if it isn't already
    if [[ "$SHELL" != "$(command -v zsh)" ]]; then
        log_info "setting zsh as default shell..."
        chsh -s "$(command -v zsh)" "$TARGET_USER"
        log_ok "default shell set to zsh (takes effect on next login)"
    else
        log_ok "zsh is already the default shell"
    fi

    echo ""
}

# в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
#  PHASE 4 вҖ” STOW DOTFILES
# в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
stow_dotfiles() {
    echo -e "${GOLD}в”Җв”Җ Stowing dotfiles в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ${NC}"

    # oh-my-zsh installs its own .zshrc вҖ” remove it before stowing ours
    if [[ -f "$TARGET_HOME/.zshrc" && ! -L "$TARGET_HOME/.zshrc" ]]; then
        local backup="$TARGET_HOME/.zshrc.bak.$(date +%s)"
        mv "$TARGET_HOME/.zshrc" "$backup"
        log_warn "moved oh-my-zsh default .zshrc вҶ’ $backup"
    fi

    local packages=(
        btop
        cava
        fastfetch
        ghostty
        gtk
        hypr
        kitty
        lazygit
        rofi
        swaync
        waybar
        wlogout
        yazi
        zsh
        themes
    )

    for pkg in "${packages[@]}"; do
        stow_package "$pkg"
    done

    # Hyprland v0.55+ overwrites hyprland.conf on first launch for users that are installing / cloning a new dotfile 
    # even through symlinks into the stow repo. Make it read-only to prevent this.
    local hypr_conf="$TARGET_HOME/.config/hypr/hyprland.conf"
    if [[ -L "$TARGET_HOME/.config/hypr" && -f "$hypr_conf" ]]; then
        chmod 444 "$hypr_conf"
        log_ok "hyprland.conf protected from overwrite (chmod 444)"
    fi

    echo ""
}

